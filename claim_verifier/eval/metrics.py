"""T8.1 / T8.2 — Evaluation metrics for the verification pipeline.

TP/FP/FN definitions (per field, detection of discrepancies):
  - Positive class: field has a real discrepancy (expected_verdict == "MISMATCH")
  - Negative class: field is correct         (expected_verdict == "MATCH")

  TP: pipeline predicts MISMATCH, truth is MISMATCH   → correct detection
  FP: pipeline predicts MISMATCH, truth is MATCH       → false alarm
  FN: pipeline predicts MATCH, truth is MISMATCH       → missed fraud
      pipeline predicts MISSING, truth is MISMATCH     → missed (extraction failed)
  TN: pipeline predicts MATCH or MISSING, truth MATCH  → correct (MISSING = not compared)

n_missing: MISSING predictions on MISMATCH ground-truth (a subset of FN);
  surfaced separately so callers can distinguish extraction failure from wrong verdict.

Case-level FP rate on clean cases:
  FP case = at least one MISMATCH verdict on a field that should MATCH, for a CLEAN case.
  FP rate = FP cases / total clean cases.
"""
from __future__ import annotations

from dataclasses import dataclass

_LEAKAGE_CAVEAT = (
    "Synthetic metrics are an upper bound. Transcripts were generated by the same "
    "template engine; real-world speech will be noisier. Treat precision/recall "
    "as best-case figures. Holdout metrics are more realistic."
)

_TARGETS = {"precision": 0.80, "fp_rate": 0.15}

_ALL_FIELDS = [
    "hospital_name",
    "admission_date",
    "discharge_date",
    "diagnosis",
    "billed_amount",
    "length_of_stay",
]


@dataclass
class FieldMetrics:
    field: str
    tp: int
    fp: int
    fn: int
    tn: int
    n_missing: int       # MISSING predictions where truth was MISMATCH (subset of FN)
    precision: float | None  # None when no positive predictions were made
    recall: float | None     # None when no positive cases exist in ground truth
    f1: float | None


@dataclass
class EvalSummary:
    n_cases: int
    n_clean: int
    n_error: int
    field_metrics: dict[str, FieldMetrics]
    fp_rate_clean: float


def compute_field_metrics(
    field: str,
    predictions: list[str],    # "MATCH" | "MISMATCH" | "MISSING" per case
    ground_truth: list[str],   # "MATCH" | "MISMATCH" per case
) -> FieldMetrics:
    """Compute confusion-matrix metrics for one field across all cases."""
    tp = fp = fn = tn = n_missing = 0
    for pred, truth in zip(predictions, ground_truth):
        if truth == "MISMATCH":
            if pred == "MISMATCH":
                tp += 1
            else:  # MATCH or MISSING — both are a miss
                fn += 1
                if pred == "MISSING":
                    n_missing += 1
        else:  # truth == "MATCH"
            if pred == "MISMATCH":
                fp += 1
            else:  # MATCH or MISSING — no false alarm
                tn += 1

    precision = tp / (tp + fp) if (tp + fp) > 0 else None
    recall = tp / (tp + fn) if (tp + fn) > 0 else None
    if precision and recall and (precision + recall) > 0:
        f1: float | None = 2 * precision * recall / (precision + recall)
    else:
        f1 = None

    return FieldMetrics(
        field=field,
        tp=tp, fp=fp, fn=fn, tn=tn,
        n_missing=n_missing,
        precision=precision,
        recall=recall,
        f1=f1,
    )


def compute_fp_rate_on_clean(
    predictions: list[dict[str, str]],
    records: list[dict],
) -> float:
    """Case-level FP rate: fraction of CLEAN cases with ≥1 spurious MISMATCH.

    predictions: [{field: status}] per case
    records:     ground-truth records (with error_type and expected_verdicts keys)
    """
    clean_indices = [i for i, r in enumerate(records) if r.get("error_type") == "CLEAN"]
    if not clean_indices:
        return 0.0

    fp_count = 0
    for i in clean_indices:
        pred = predictions[i]
        truth = records[i]["expected_verdicts"]
        # Any MISMATCH on a should-MATCH field is a false positive
        if any(pred.get(f, "MISSING") == "MISMATCH" for f in truth if truth[f] == "MATCH"):
            fp_count += 1

    return fp_count / len(clean_indices)


def compute_eval_summary(
    records: list[dict],
    pipeline_verdicts: list[dict[str, str]],
) -> EvalSummary:
    """Aggregate metrics over all cases.

    records:          ground-truth records from ground_truth.jsonl
    pipeline_verdicts: [{field: status}] per case, from run_eval
    """
    field_metrics: dict[str, FieldMetrics] = {}
    for field in _ALL_FIELDS:
        preds = [v.get(field, "MISSING") for v in pipeline_verdicts]
        truths = [r["expected_verdicts"].get(field, "MATCH") for r in records]
        field_metrics[field] = compute_field_metrics(field, preds, truths)

    fp_rate = compute_fp_rate_on_clean(pipeline_verdicts, records)
    n_clean = sum(1 for r in records if r.get("error_type") == "CLEAN")

    return EvalSummary(
        n_cases=len(records),
        n_clean=n_clean,
        n_error=len(records) - n_clean,
        field_metrics=field_metrics,
        fp_rate_clean=fp_rate,
    )


def _pf(value: float | None, decimals: int = 2) -> str:
    return f"{value:.{decimals}f}" if value is not None else "N/A"


def format_report(
    summary: EvalSummary,
    holdout_summary: EvalSummary | None = None,
    label: str = "Synthetic",
) -> str:
    """Return a human-readable eval report string."""
    lines: list[str] = ["=== Evaluation Report ===", ""]

    lines.append(
        f"{label} dataset: {summary.n_cases} cases "
        f"({summary.n_clean} clean, {summary.n_error} error)"
    )
    lines.append("")
    lines.append("Per-field metrics:")
    for field, fm in summary.field_metrics.items():
        lines.append(
            f"  {field:<25} precision={_pf(fm.precision)}  recall={_pf(fm.recall)}"
            f"  f1={_pf(fm.f1)}  "
            f"tp={fm.tp} fp={fm.fp} fn={fm.fn} tn={fm.tn}"
        )

    all_tp = sum(fm.tp for fm in summary.field_metrics.values())
    all_fp = sum(fm.fp for fm in summary.field_metrics.values())
    all_fn = sum(fm.fn for fm in summary.field_metrics.values())
    agg_prec = all_tp / (all_tp + all_fp) if (all_tp + all_fp) > 0 else None
    agg_rec = all_tp / (all_tp + all_fn) if (all_tp + all_fn) > 0 else None

    lines.append("")
    lines.append(f"Aggregate: precision={_pf(agg_prec)}  recall={_pf(agg_rec)}")
    lines.append(f"FP rate on clean cases (case-level): {_pf(summary.fp_rate_clean)}")

    lines.append("")
    lines.append("Targets (relaxed for local 7B model):")
    prec_ok = agg_prec is not None and agg_prec >= _TARGETS["precision"]
    fp_ok = summary.fp_rate_clean <= _TARGETS["fp_rate"]
    lines.append(f"  precision >= {_TARGETS['precision']:.0%}: {'PASS' if prec_ok else 'FAIL'}")
    lines.append(f"  FP rate   <= {_TARGETS['fp_rate']:.0%}: {'PASS' if fp_ok else 'FAIL'}")

    if holdout_summary is not None:
        lines.append("")
        h_tp = sum(fm.tp for fm in holdout_summary.field_metrics.values())
        h_fp = sum(fm.fp for fm in holdout_summary.field_metrics.values())
        h_fn = sum(fm.fn for fm in holdout_summary.field_metrics.values())
        h_prec = h_tp / (h_tp + h_fp) if (h_tp + h_fp) > 0 else None
        h_rec = h_tp / (h_tp + h_fn) if (h_tp + h_fn) > 0 else None
        lines.append(
            f"Holdout ({holdout_summary.n_cases} cases): "
            f"precision={_pf(h_prec)}  recall={_pf(h_rec)}  "
            f"FP rate={_pf(holdout_summary.fp_rate_clean)}"
        )

    lines.append("")
    lines.append("LEAKAGE CAVEAT")
    lines.append(_LEAKAGE_CAVEAT)

    return "\n".join(lines)
